def iter_destinations() -> Iterator[dict[str, str]]:
    """Yield every destination as a flat dict with country/region/sub_region."""
    for country, places in DESTINATIONS.items():
        region = REGION_OF[country]
        for name, sub_region in places:
            yield {
                "name": name,
                "country": country,
                "region": region,
                "sub_region": sub_region,
            }


def total() -> int:
    return sum(len(places) for places in DESTINATIONS.values())


if __name__ == "__main__":
    n = total()
    print(f"Total destinations: {n}")
    by_region: dict[str, int] = {}
    for d in iter_destinations():
        by_region[d["region"]] = by_region.get(d["region"], 0) + 1
    print("By region:")
    for region, count in sorted(by_region.items(), key=lambda x: -x[1]):
        print(f"  {region}: {count}")
